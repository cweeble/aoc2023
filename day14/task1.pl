#!/usr/bin/perl
use strict;
my @data = (<DATA>);
chomp @data;
my @dataLines;
for my $line (@data) {
    my @line = split(//, $line);
    push @dataLines, \@line;
}
for (my $i=1; $i < @dataLines; $i++) {
    my @line = @{$dataLines[$i]};
    for (my $j=0; $j < @line; $j++) {
        my $c = $line[$j];
        next if ($c ne 'O');
        for (my $k=$i-1; $k>=0; $k--) {
            next if (($dataLines[$k]->[$j] eq '.') and ($k));
            $k++ if ($dataLines[$k]->[$j] ne '.');
            last if ($k == $i);
            $dataLines[$k]->[$j] = $c;
            $dataLines[$i]->[$j] = '.';
            last;
        }
    }
}
my $answer = 0;
my $lineNo = 0;
my $hoes;
for my $dataLineRef (reverse @dataLines) {
    my $line = join('', @{$dataLineRef});
    my $count = $line=~ s/O/o/g;
    my $score = $count * ++$lineNo;
    $answer += $score;
    $hoes += $count;
    printf ("%3d:%s %d %d %d\n", $lineNo, $line, $count, $score, $answer);
}
print "$answer\n";
